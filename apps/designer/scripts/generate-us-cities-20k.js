/**
 * Generates `src/config/seo/usCities20k.generated.ts` from GeoNames `cities15000.zip`.
 *
 * Source: https://download.geonames.org/export/dump/
 * License: GeoNames data is available under CC BY 4.0 (attribution required).
 *
 * Usage:
 *   node scripts/generate-us-cities-20k.js
 *
 * Optional env:
 *   MIN_POP=20000
 *   ZIP_URL=https://download.geonames.org/export/dump/cities15000.zip
 */

const fs = require("fs");
const os = require("os");
const path = require("path");
const { execFileSync } = require("child_process");

const ZIP_URL =
  process.env.ZIP_URL || "https://download.geonames.org/export/dump/cities15000.zip";
const MIN_POP = Number(process.env.MIN_POP || 20000);

const REPO_ROOT = path.resolve(__dirname, "..");
const OUT_FILE = path.join(REPO_ROOT, "src/config/seo/usCities20k.generated.ts");

// Workaround: this environment intermittently blocks DNS (getaddrinfo).
// For the GeoNames host we provide a stable fallback IP for `curl --resolve`.
// You can override via `GEONAMES_DOWNLOAD_IP`.
const KNOWN_HOST_IP_OVERRIDES = new Map([
  ["download.geonames.org", process.env.GEONAMES_DOWNLOAD_IP || "5.9.152.54"],
]);

function toSlug(value) {
  return String(value || "")
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function downloadToFile(url, outPath) {
  const u = new URL(url);
  const hostname = u.hostname;
  const overrideIp = KNOWN_HOST_IP_OVERRIDES.get(hostname);
  const args = ["-L", "--fail", "--silent", "--show-error"];
  if (overrideIp) args.push("--resolve", `${hostname}:443:${overrideIp}`);
  args.push("-o", outPath, url);

  execFileSync("curl", args, { stdio: "inherit" });
  const stat = fs.statSync(outPath);
  if (!stat.isFile() || stat.size < 1000) {
    throw new Error(`Download output looks wrong (size=${stat.size}). File: ${outPath}`);
  }
}

function unzipFirstTxt(zipPath) {
  const list = execFileSync("unzip", ["-Z1", zipPath], { encoding: "utf8" })
    .split("\n")
    .map((l) => l.trim())
    .filter(Boolean);
  const txtName = list.find((n) => n.toLowerCase().endsWith(".txt"));
  if (!txtName) throw new Error(`No .txt found in zip: ${zipPath}`);
  const content = execFileSync("unzip", ["-p", zipPath, txtName], { encoding: "utf8", maxBuffer: 1024 * 1024 * 200 });
  return { txtName, content };
}

function parseGeoNamesCities(txt) {
  const rows = [];
  const lines = txt.split(/\r?\n/);
  for (const line of lines) {
    if (!line) continue;
    const cols = line.split("\t");
    if (cols.length < 19) continue;
    const geonameId = Number(cols[0] || 0);
    const name = cols[1] || "";
    const asciiname = cols[2] || name;
    const lat = Number(cols[4] || 0);
    const lng = Number(cols[5] || 0);
    const featureCode = cols[7] || "";
    const countryCode = cols[8] || "";
    const admin1 = cols[10] || "";
    const population = Number(cols[14] || 0);

    rows.push({
      admin1,
      asciiname,
      countryCode,
      featureCode,
      geonameId,
      lat,
      lng,
      name,
      population,
    });
  }
  return rows;
}

function main() {
  const tmpZip = path.join(os.tmpdir(), `geonames-cities15000-${Date.now()}.zip`);
  console.log(`Downloading ${ZIP_URL} ...`);
  try {
    downloadToFile(ZIP_URL, tmpZip);
    const { txtName, content } = unzipFirstTxt(tmpZip);
    console.log(`Parsing ${txtName} ...`);
    const rows = parseGeoNamesCities(content);

    const seen = new Set();
    const cities = rows
      .filter((r) => r.countryCode === "US")
      .filter((r) => r.population >= MIN_POP)
      .filter((r) => /^[A-Z]{2}$/.test(String(r.admin1 || "").toUpperCase()))
      .map((r) => {
        const state = String(r.admin1 || "").toUpperCase();
        const city = String(r.asciiname || r.name || "").trim();
        const cityStateSlug = `${toSlug(city)}-${state.toLowerCase()}`;
        return {
          city,
          cityStateSlug,
          geonameId: r.geonameId,
          lat: r.lat,
          lng: r.lng,
          population: r.population,
          state,
        };
      })
      .filter((c) => {
        const key = `${c.city.toLowerCase()}|${c.state}`;
        if (seen.has(key)) return false;
        seen.add(key);
        return true;
      })
      .sort((a, b) => {
        if (b.population !== a.population) return b.population - a.population;
        if (a.state !== b.state) return a.state.localeCompare(b.state);
        return a.city.localeCompare(b.city);
      });

    const generatedAt = new Date().toISOString();
    const header =
      `/* eslint-disable */\n` +
      `// Generated by scripts/generate-us-cities-20k.js on ${generatedAt}\n` +
      `// Source: GeoNames cities15000.zip (${ZIP_URL})\n` +
      `// License: CC BY 4.0 (https://www.geonames.org/)\n\n`;

    const body =
      `export type UsCity20k = {\n` +
      `  city: string;\n` +
      `  state: string;\n` +
      `  population: number;\n` +
      `  geonameId: number;\n` +
      `  lat: number;\n` +
      `  lng: number;\n` +
      `  cityStateSlug: string;\n` +
      `};\n\n` +
      `export const US_CITIES_20K: UsCity20k[] = ${JSON.stringify(cities, null, 2)};\n`;

    fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
    fs.writeFileSync(OUT_FILE, header + body, "utf8");

    console.log(`Wrote ${OUT_FILE}`);
    console.log(`Count: ${cities.length.toLocaleString()} (US, pop >= ${MIN_POP.toLocaleString()})`);
  } finally {
    try {
      fs.unlinkSync(tmpZip);
    } catch {}
  }
}

try {
  main();
} catch (err) {
  console.error(err);
  process.exit(1);
}
